#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int	dest_len;
	int	i;

	dest_len = ft_strlen(s1);
	i = 0;
	while (s2[i] != 0 && n--)
	{
		s1[dest_len + i] = s2[i];
		i++;
	}
	s1[dest_len + i] = '\0';
	return (s1);
}
